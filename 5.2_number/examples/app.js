//Проверка: isFinite, isNaN, NaN - предстовляет ошибку, Infinity - математическая бесконечность.

//isNaN(value) преобразует значение в число и проверяет является ли оно NaN
alert(isNaN(NaN))   //true
alert(isNaN('str')) //true

//isFinite преобразует аргумент в число и возвращает true, если оно является обычным числом, т.е. не NaN/Infinity/-Infinity

alert(isFinite('5'));      //true
alert(isFinite('str'));    //false, потому что специальное значение NaN
alert(isFinite(Infinity)); //false, потому что специальное значение Infinity





//parseInt, parseFloat, если эти функции не смогли вернуть число, они вернут NaN

//parseInt возвращает целое число
alert(parseInt('100px'));  //100
alert(parseInt('11.5'));   //11 выводом будет только целая часть 11,

//parseFloat возвращает число с плавающей точкой 
alert(parseFloat('12.3e5'));   //12.3
alert(parseFloat('12.3.4'));   //12.3 произойдет остановка чтения на второй точке


//Другие математические ф-ции

//Math(random) возвращает псевдослучайное число в диапазоне от 0 (включительно) до 1 (но не включая 1)
alert(Math.random()) // 0.31232131242

//Math.max/min(a, b, c, ...)  возвращает наибольшее/наименьшее число из перечисленных
alert(Math.max(2, 3, -4, 8)) //8

//Math.pow(n, power) возвращает число n, возведенное в степень power
alert(Math.pow(2, 10))  //1024








