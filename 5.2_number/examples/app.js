// //toFixed(n) - округляет число до n знаков после запятой и возвращает строковое представление результата:

let num = 1.234;
alert(num.toFixed(1));  //1.2
//Он также округляет значение до ближайшего числа, как в большую, так и в меньшую сторону
let numb3 = 12.36;
